public class BeginEnd {
    private int begin;
    private int end;

    public BeginEnd(int begin, int end) {
        this.setBegin(begin);
        this.setEnd(end);
    }

    public int getBegin() {
        return this.begin;
    }

    public void setBegin(int begin) {
        this.begin = begin;
    }

    public int getEnd() {
        return this.end;
    }

    public void setEnd(int end) {
        this.end = end;
    }
}
