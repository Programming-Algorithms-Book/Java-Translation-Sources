
public class MinResult {
    private int min1;
    private int min2;
    
    public MinResult(int min1, int min2) {
        this.setMin1(min1);
        this.setMin2(min2);
    }
    
    public int getMin1() {
        return this.min1;
    }
    
    public void setMin1(int min1) {
        this.min1 = min1;
    }
    
    public int getMin2() {
        return this.min2;
    }
    
    public void setMin2(int min2) {
        this.min2 = min2;
    }
}
